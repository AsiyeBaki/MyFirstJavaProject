package day16_loopContinue;

public class NestedLoopIntro {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {  // it will print 25 times

            for (int j = 0; j < 5; j++) {
                System.out.println("Wooden Spoon");
            }


        }
    }
}
